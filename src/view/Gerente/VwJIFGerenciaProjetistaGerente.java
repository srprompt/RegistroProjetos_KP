                       "VALUES ('"+ txtCodigo.getText() +"','"+ txtNome.getText() +"',"
                        + "'"+ dc_nasc.getDate() +"')";
            
            bd.stmt.executeUpdate(sql);
            bd.Desconectar();
            
            
