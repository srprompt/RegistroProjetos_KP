     private ArrayList<Projetista> projetistaList(){
       ArrayList<Projetista> projetistasList = new ArrayList<>();
       
       try{
            ConexaoBD bd = new ConexaoBD();

            conectaBanco(Constante.servidor, bd, Constante.login, Constante.login); 
             
            String sql = "SELECT id, cod_funcional, nome, data_nasc FROM private.projetista;" ;
            
            ResultSet rs = bd.stmt.executeQuery(sql);
            Projetista projetista;
            while(rs.next()){
                //projetista = new Projetista (rs.getInt("id"), rs.getInt("Cod_funcional"), rs.getString("nome"), rs.getDate("data_nasc"));
                Projetista pr = new Projetista();
                pr.setId(rs.getInt("id"));
                pr.setCodigo_funcional(rs.getInt("Cod_funcional"));
                pr.setNome(rs.getString("nome"));
                pr.setData_nasc(rs.getDate("data_nasc"));
                
                projetistasList.add(pr);
            }
            
            bd.Desconectar();
            
       }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
       
       return projetistasList;
   }
    
    private void mostra_projetista(){
        ArrayList<Projetista> list = projetistaList();
        DefaultTableModel model = (DefaultTableModel)jt_MostraProjetistas.getModel();
        Object[] row = new Object[4];
        for(int i=0; i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getCodigo_funcional();
            row[2]=list.get(i).getNome();
            row[3]=list.get(i).getData_nasc();
            model.addRow(row);
        }
        Tabela.ordenaTabela(jt_MostraProjetistas);
    }
