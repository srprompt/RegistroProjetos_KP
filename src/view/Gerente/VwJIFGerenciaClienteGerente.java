    private ArrayList<Cliente> clienteList(){
       ArrayList<Cliente> clientesList = new ArrayList<>();
       
       try{
            ConexaoBD bd = new ConexaoBD();
            
           
            conectaBanco(Constante.servidor, bd, Constante.login, Constante.login);
             
            String sql = "SELECT id, cpf, nome, data_nasc FROM private.cliente;" ;
            
            ResultSet rs = bd.stmt.executeQuery(sql);
            Cliente cliente;
            while(rs.next()){
                //projetista = new Projetista (rs.getInt("id"), rs.getInt("Cod_funcional"), rs.getString("nome"), rs.getDate("data_nasc"));
                Cliente cl = new Cliente();
                cl.setId(rs.getInt("id"));
                cl.setCpf(rs.getString("cpf"));
                cl.setNome(rs.getString("nome"));
                cl.setData_nasc(rs.getDate("data_nasc"));
                
                clientesList.add(cl);
            }
            
            bd.Desconectar();
            
       }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
       
       return clientesList;
   }
    
        private void mostra_cliente() {
        ArrayList<Cliente> list = clienteList();
        DefaultTableModel model = (DefaultTableModel)jt_MostraClientes.getModel();
        Object[] row = new Object[4];
        for(int i=0; i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getCpf();
            row[2]=list.get(i).getNome();
            row[3]=list.get(i).getData_nasc();
            model.addRow(row);
        }
        Tabela.ordenaTabela(jt_MostraClientes);
    }
